#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	digit;
	long	nb;

	nb = (long) n;
	if (nb < 0)
	{
		nb *= -1;
		write(fd, "-", 1);
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	if (nb < 10)
	{
		digit = nb + '0';
		write(fd, &digit, 1);
	}
}
